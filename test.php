বিষয়: দরখাস্তকারী পক্ষে দেওয়ানী কার্যবিধি ১৯০৮ এর আদেশ ১ নিয়ম১০(২) ও ধারা ১৫১ মতে মৃত ১ - ৭ নং বাদিগণের নিম্নোক্ত ওয়ারিশগণকে বাদীর সারিতে পক্ষভূক্তির দরখাস্ত।

বিজ্ঞ আদালতে দরখাস্তকরীর পক্ষে নিবেদন এই যে,
(১)
অদ্য সূত্রাধীন মামলাটির ইস্যু গঠনের দিন ধার্য্য নহে মর্মে দরখাস্তকারী স্বার্থে আবশ্যকতায় আদ্য তারিখ--------------- বিজ্ঞ আদলতে ফাইল ফুটআপের মাধ্যমে অত্র পক্ষভুক্তির দরখাস্ত উপস্থাপন করত: যেহেতু অত্র মামলার আর্জি ও প্রার্থনা আদলতে দাখিল করার সময় অনিবার্য কারণ বশত আমরা বাদিশ্রণীতে পক্ষভু্ক্তগণ অবগত না থাকায় অত্র মামলার বাদি শ্রেণীতে বাদ পরে যাওয়ায় বিজ্ঞ আদালতে ওয়ারিশ সনদ ও সাধারণ আমমোক্তার নামার মাধ্যমে মামলার বাদি হওয়ায় একান্ত আবশ্যক অন্যথায় দরখাস্তকারীগণের অপূরণীয় ক্ষতি হইবে। ওয়ারীশসনদ ও সাধারণ আমমোক্তার নামার অনুবলে অত্র দরখাস্তকারীগণকে বাদিগণের স্থলে স্থলাভিষিক্ত করার দরখাস্ত আনায়ন করা হল বিধায় ন্যায় বিচারের স্বার্থে অত্র নালিশি মামলার নালিশি সম্পত্তির আর্জি হইতে বাদ পরে যাওয়া ওয়ারিশগণকে সূত্রাদিন মামলার পক্ষভুক্ত করা একান্ত আবশ্যক।

অতএব প্রার্থনা এই যে, উপরোল্লিখিত বিষয়ের আলোকে দরখাস্তকারী বৈধ ওয়ারিশদেরকে পক্ষভূক্তকরার আদেশদানে হুজুর আদালতের বিহিত মর্জি হয়।
(২)
তফসিলউক্ত নালিশি জমির ওয়ারিশ সূত্রমতে আর্জির ১৯ নং পৃষ্ঠার ৫ নং অনুচ্ছেদের অনুবলে লিখিত ৪ নং লাইনের চিহ্নিত উক্ত হযরত আরহামউল্লাহ প্রকাশ আমানউল্লাহ মরণে তাহার তাজ্যভিত্তিক একপুত্র(সংশোধন-দুই পুত্র) যথা - ১/ মৌলানা ছৈয়দউল্লাহ ২/ মৌলানা বশির উল্লাহ প্রাপ্ত হয়। উক্ত সৈয়দ উল্লাহর ওয়ারিশান সূত্র আর্জিতে অন্তর্ভুক্ত রহিয়াছে। তৎমতে আরহাম উল্লাহর দ্বিতীয়পুত্র মৌলানা বশির উল্লাহর দুই পুত্র মনির উল্লাহ ও মুজিবউল্লাহ। মুজিব উল্লাহ মরণে পুত্র সিদ্দিক আহমদ ও কন্যা ছমুদা খাতুন ওয়ারিশ থাকেন। উক্ত ছমুদা খাতুন মরণে দুই পুত্র যথা ১/ আহমুদুর রহমান ২/ মাহমুদুর রহমান এবং দুই কন্যা যথা ১/ মাহমুদা খাতুন ও ২/ হাজেরা খাতুন ওয়ারিশ থাকে। উক্ত মাহমুদুর রহমান মরণে ১ স্ত্রী রাবেয়া খাতুন, ১ পুত্র আবুল কাশেম ও ৬ কন্যা (দরখাস্তকারীগণের পক্ষে সাধারণ আমমোক্তার গ্রহীতা আবুল কাশেম গং) যথা ১/ ফরিদা খাতুন ২/ রুবি আক্তার ৩/ লাকি আক্তার ৪/ মুন্নি আক্তার ৫/ পারভিন আক্তার ৬/ লায়লা খাতুন।
মাহমুদা খাতুন মরণে ৫ পূত্র যথা ১/ আহমদ হোসেন ২/কাজী মাহমুদুল হক ৩/ কাজী আমানুল হক ৪/ কাজী মুবিনুল হক ও ৫/ কাজী মাহফুজুল হক এবং ৩ কন্যা যথা ১/ মৃত মোস্তফা খাতুন ২/ সালমা খাতুন ৩/ রোকেয়া বেগম ওয়ারিশ থাকে। মাহমুদুল হক ইন্তেকালে ১ পুত্র কাজী হাবিব উল্লাহ ও ১ স্ত্রী খুরশীদা বেগম এবং ২ কন্যা যথা ১/ আয়েশা আরবী ২/ আসমা আক্তার ওয়ারিশ থাকে। আবার মোস্তফা খাতুন ইন্তেকালে ২ পুত্র যথা ১/ফখরুল ইসলাম ২/ দিদারুল ইসলাম ও ৫ কন্যা যথা ১/ রাবেয়া বেগম ২/ তহমিনা বেগম ৩/ তসলিমা বেগম ৪/ নাসরিন সুলতানা ৪/ মৃত রাশেদা বেগম(নি:সন্তান) ওয়ারিশ থাকেন। হাজেরা খাতুন মরণে ৩ পুত্র যথা ১/ নূর আহমদ ২/ হাফেজ আহমদ ৩/ নরুল আবছার এবং ২ কন্যা যথা ১/মৃত মোস্তফা খাতুন ২/ মোবারক খাতুন ওয়ারিশ থাকেন। মোস্তফা খাতুন মরণে ৫ পুত্র যথা- ১/ আবদুল মান্নান ২/ আবদুস সবুর ৩/ আবদুল করিম ৪/ আবদুর রহিম ৫/ মৃত আবদুল কাদের। আবদুল কাদের মরণে ১ পুত্র মো.ইয়াসিন এবং ৫ কন্যা যথা - ১/ রায়হানা বেগম ২/ তাহেরা বেগম ৩/ মাইমুনা বেগম ৪/ সামশুন্নাহার ৫/নুরুন্নাহার ওয়ারিশ থাকেন।

আর্জির ২০ পৃষ্ঠার ৭ নং অনুচ্ছেদের অনুবলে ১ম লাইনের হাজি গরিবুল্লাহ ওয়ারিশদ্বয়ের বাদি শ্রেণী হতে বাদ পরে যাওয়া ওয়ারিশগনের মধ্যে আব্দুল হাকিম পিং হাজি গরিবুল্লাহ, আবদুর রাজ্জাক পিং আব্দুল হাকিম। উক্ত আব্দুর রাজ্জাক মরণে ২ পুত্র যথা - ১/ আবদুল হাফেজ ও ২/ আলি আহমদ। উক্ত আবদুল হাফিজ মরণে ২ পুত্র যথা ১/ খানে আলম ২/ আবদুল ওয়াজেদ লেদু। উক্ত আলি আহমদ মরণে একমাত্র কন্যা জোবেদা খাতুন ওয়ারিশ হন। জোবেদা খাতুন মরণে ১ পুত্র ও ২ কন্যা, পুত্রদ্বয়ের ১/ আহমদ কবির ও কন্যাগণ ১/ ওয়াজ খাতুন ২/ হাবিবা খাতুন ওয়ারিশ হন। উক্ত আহমদ কবির মরণে ৪ পুত্র, ৫ কন্যা ও ২ স্ত্রী, পুত্রগণ ১/ জাহাঙ্গীর আলম ২/ আবদুল জলিল ৩/ মৌলানা মুসলিম উদ্দিন কাবিরি( ওয়ারিশদ্বয়ের সাধারণ আমমোক্তার গ্রহীতা) ৪/ আলি আহমদ কাবিরি। কন্যদ্বয় ১/ জাহানারা বেগম ২/ আনোয়ার কলি ৩/ বকুল ৪/ জোবেদান্নাহা ৫/ রোজিনা আক্তার । স্ত্রী ১/ আনোয়ারা ২/ মৃত রাজিয়া খাতুন।

