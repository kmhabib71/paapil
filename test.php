০২| দলিলে পক্ষগণের অভিপ্রায় প্রতিফলনের প্রয়োজনীয় তথ্যাদি সহ পাওয়ার দাতা কর্তৃক প্রদেয় অপ্রত্যাহারযোগ্য ক্ষমতা এবং পাওয়ার গ্রহীতা কর্তৃক সম্পাদিতব্য দায়িত্ব ও কর্তব্য সমূহের বিস্তারিত বিবরণ:

(ক) অদ্য হইতে অত্র আম-মোক্তারনামা দলিলের অনুবলে তুমি/আপনি আম-মোক্তারনামা দলিল গ্রহীতা আমি/আমরা আম-মোক্তার নামা দাতার মালিকানাধীন নিম্ন তফসিলে বিশদভাবে বর্ণিত সমুদয় সম্পত্তি দেখাশুনা, শাসন-সংরক্ষণ ও পরিচালনা করিবে। নিম্ন তফসিল বর্ণীত জমির রেকর্ডপত্র হাল নাগাদ করিবে। তফসিল বর্ণিত সংশ্লিষ্ট সম্পত্তি সংক্রান্তে যাবতীয় দিললপত্র হালনাগাদ করনার্থে কিংবা দলিলপত্র সংশােধনার্থে কোন উপযুক্ত এখতিয়ার সম্পন্ন দেওয়ানী আদালতে মামলা করার প্রয়ােজন হইলে তাহাও করিবে । তুমি অফিস কিংবা রাজস্ব অফিস, সরকারী-বেসরকারী, কোন স্বায়ত্ব-শাসিত, আধা স্বায়ত্বশাসিত কিংবা সহকারী ইউনিয়ন ভূমি অফিসে গমন করিয়া নামজারী জমাভাগ মামলা সহ নামজারী খতিয়ান সৃজন, খাজনা, উন্নয়ন করাদি, চার্জ প্রদান কিংবা অন্য কোন প্রয়ােজনে সরকারী আফিসে কোন মামলা বা দরখাস্ত করার কিংবা আমার পক্ষে দস্তখত করার প্রয়ােজন হলে কিংবা প্রয়ােজনীয় পদক্ষেপ গ্রহণ করার প্রয়ােজন হইলে তুমি আম-মােক্তারনামা গ্রহীতা যখন যাহা প্রয়ােজন তাহা করিতে পারিবে।
(খ) নিম্ন তফসিলােক্ত জমি জমার সংক্রান্তে অদ্ভুত যেকোন মামলায় আমার পক্ষে তুমি/আপনি আমার/ আমাদের আম-মােক্তার হিসাবে আপত্তি, বর্ণনা, শপথনামা ও ওকালতনামা দরখাস্ত ইত্যাদিতে দস্তখত করিবে, দেশের সর্বোচ্চ আদালত সহ সর্বদালতে উপস্থিত হইয়া আমার আমাদের পক্ষে মামলা পরিচালনা করিবে, সাক্ষ্য ও জোবানবন্দি প্রদান করিবে, ওকালতনামায় দস্তখত করিয়া উকিল বা প্লিডার বা এটর্নি নিয়ােগ কিংবা বাতিল কিংবা পুনঃ নিয়ােগ করাসহ, আর্জি, জবাব, আর্জি সংশােধন, জবাব সংশােধন, আপত্তি, দরখাস্ত, হাজিরা ইত্যাদি স্বাক্ষর পূর্বক দায়ের ও মোকাবিলা করিতে পারিবে। সাক্ষী, জবানবন্দি, জেরা, হলফনামা, সত্যপাঠ ইত্যাদিতে টিপ/দস্তখত প্রদান পূর্বক যাবতীয় মামলা প্রতিদ্বন্দ্বিতা এবং মামলা পরিচালনা করিয়া চূড়ান্ত নিষ্পত্তি করিয়া লইতে সক্ষম থাকিবে এবং সােলেহনমা প্রদান পূর্বক আপোষ নিষ্পত্তি করিতে পারিবে ।
